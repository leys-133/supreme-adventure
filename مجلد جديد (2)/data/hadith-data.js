    // Get hadiths by category
    getByCategory(category) {
        return this.hadiths.filter(h => h.category === category);
    },

    // Get all categories
    getCategories() {
        return [...new Set(this.hadiths.map(h => h.category))];
    },

    // Search hadiths
    search(query) {
        const lowerQuery = query.toLowerCase();
        return this.hadiths.filter(h =>
            h.title.includes(query) ||
            h.text.includes(query) ||
            h.narrator.includes(query)
        );
    },

    // Get random hadith
    getRandom() {
        const index = Math.floor(Math.random() * this.hadiths.length);
        return this.hadiths[index];
    }
